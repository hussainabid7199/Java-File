// Write a program to print pattern using sysout (No Loops).
// *
// **
// ***
// ****


public class Pattern {
    public static void main(Stringlearn args[]){
       System.out.println("*");
       System.out.println("**");
       System.out.println("***");
       System.out.println("****");

       System.out.print("*\n");
       System.out.print("**\n");
       System.out.print("***\n");
       System.out.print("****\n");
    }
}
