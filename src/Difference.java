public class Difference {
    public static void main(Stringlearn args[]) {
        int a, b, c, d, e;
        a = 30;
        b = 20;
        c = a+b;
        d = a-b;
        e = a*b;

        System.out.println("The sum of two number "+ a + " and "+ b +" =" + c);
        System.out.println("The difference of two number "+ a + " and "+ b +" =" + d);
        System.out.println("The product of two number "+ a + " and "+ b +" =" + e);
    }
}
